package cdo.Datos;

public class Correos {
	private String correo;

	public Correos(String correo) {
		super();
		this.correo = correo;
	}
	
	public Correos() {
		this.correo = "";
	}

	public String getCorreo() {
		return correo;
	}

	public void setCorreo(String correo) {
		this.correo = correo;
	} 
	
	
}
